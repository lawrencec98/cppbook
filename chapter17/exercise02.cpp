/*
    How many bytes are there in an: int, double, bool, char?
*/

#include "../std_lib_facilities.h"


int main() {

    cout << "int: " << sizeof(int) << endl;
    cout << "double: " << sizeof(double) << endl;
    cout << "bool: " << sizeof(bool) << endl;
    cout << "char: " << sizeof(char) << endl;

    return 0;
}